int main() 
{
    int num;

    // Take input from user
    cout << "Enter a number: ";
    cin >> num;

    // Check number condition
    if (num > 0)
    {
        cout << "The number is Positive.";
    }
    else if (num < 0)
    {
        cout << "The number is Negative.";
    }
    else
    {
        cout << "The number is Zero.";
    }

    return 0;
}
