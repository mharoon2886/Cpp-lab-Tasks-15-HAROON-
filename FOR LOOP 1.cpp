#include <iostream>
using namespace std;

int main() {
    cout << "Even numbers from 2 to 20: ";
    
    // for loop to print even numbers
    for(int i = 2; i <= 20; i += 2) { // i increases by 2 each time
        cout << i << " ";
    }

    cout << endl;
    return 0;
}
