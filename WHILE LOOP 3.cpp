#include <iostream>
using namespace std;

int main()
{
    int i = 1;   // Starting value

    // Loop runs while i is less than or equal to 10
    while(i <= 10)
    {
        cout << i << " ";
        i++;   // Increase value by 1
    }

    return 0;
}
